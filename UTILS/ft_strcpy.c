#include "../HEADERS/utils.h"

void    ft_strcpy(char *dst, char *src, char c)
{
    int i;

    i = 0;
    while (src[i] && src[i] != c)
    {
        dst[i] = src[i];
        i++;
    }
    dst[i] = '\0';
}
